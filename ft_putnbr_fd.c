#include "libft.h"

void	ft_putnbr_fd(int s, int fd)
{
	char *s1;
	char *s2;

	s1 = ft_itoa(s);
	s2 = s1;
	while (s1[0])
	{
		write(fd, s1, 1);
		s1++;
	}
	free(s2);
}
